package com.example.thread_practice;

public class Post {
    private String id;
    private String userId;
    private String title;
    private String body;

    public String getId() {
        return id;
    }
    public String getUserId() {
        return userId;
    }
    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
